<h1>Chính sách bảo mật thông tin</h1>

<p>
– Thu thập thông tin chỉ cho mục đích xác minh và hỗ trợ khách hàng.<br>
– Cam kết không bán, không trao đổi dữ liệu cá nhân cho bên thứ ba.<br>
– Mọi thông tin được mã hóa và lưu trữ an toàn.<br>
– Khách hàng có quyền yêu cầu xem, chỉnh sửa hoặc xóa dữ liệu của mình.<br>
– Hệ thống thường xuyên cập nhật biện pháp bảo mật để giảm rủi ro rò rỉ.<br>
</p>
